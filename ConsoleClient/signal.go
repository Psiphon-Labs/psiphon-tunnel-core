package main

import (
	"os"
	"os/signal"
	"syscall"
)

func makeSIGUSR2Channel() chan os.Signal {
	channel := make(chan os.Signal, 1)
	signal.Notify(channel, syscall.SIGUSR2)
	return channel
}
